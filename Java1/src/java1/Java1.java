
package java1;


public class Java1 {

    public static void main(String[] args) {
        
        //for loop to iterate from 0 to 100
        for (int i = 0; i <= 100; i++) {
            /*if the remainder of ith number is 0 when 
            divided by 2, then print out the number*/
            if(i%2==0){
                System.out.println(i + " is an even number.");
            }
        }
        
    }
    
}
